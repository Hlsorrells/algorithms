// Write code to print all numbers from 1 to `num`
// Assume `num` will be a positive number
var logNums = num => {
    for (var i = 1; i <= num; i++) {
        console.log(i)
    }
};

// Write code to print all numbers from `num` down to 1
// You may assume `num` will be a positive number
var countdown = num => {
    for (var i = num; i > 0; i--){
        console.log(i)
    }
};
